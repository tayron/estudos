<?php

/**
 * Description of Calculo
 *
 * @author Tayron
 */
class Calculo 
{
    /**
     * Método que realiza a soma entre dois números
     * 
     * @param int $numeroA
     * @param int $numeroB
     * @return int
     */
    public function somar($numeroA, $numeroB)
    {
        return $numeroA + $numeroB;
    }
}
